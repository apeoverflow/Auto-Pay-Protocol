#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

const REGISTRY_FILE = 'chains.json';
const OUTPUT_FILE = '../frontend/src/config/chains.ts';

const REQUIRED_FIELDS = ['chainId', 'name', 'shortName', 'nativeCurrency', 'rpcUrl', 'usdc', 'blockExplorer', 'blockExplorerName'];

function esc(s) {
  return String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

function loadRegistry() {
  if (!fs.existsSync(REGISTRY_FILE)) {
    console.error('Error: chains.json not found');
    process.exit(1);
  }
  return JSON.parse(fs.readFileSync(REGISTRY_FILE, 'utf8'));
}

function validate(key, chain) {
  for (const field of REQUIRED_FIELDS) {
    if (chain[field] == null) {
      console.error(`Error: Chain '${key}' is missing required field: ${field}`);
      process.exit(1);
    }
  }
  const nc = chain.nativeCurrency;
  if (!nc.name || !nc.symbol || nc.decimals == null) {
    console.error(`Error: Chain '${key}' has incomplete nativeCurrency (need name, symbol, decimals)`);
    process.exit(1);
  }
}

function generate() {
  const registry = loadRegistry();

  // Filter to enabled chains
  const enabledChains = Object.entries(registry).filter(([, c]) => c.enabled !== false);

  if (enabledChains.length === 0) {
    console.error('Error: No enabled chains found in chains.json');
    process.exit(1);
  }

  // Validate all chains
  for (const [key, chain] of enabledChains) {
    validate(key, chain);
  }

  // Build env var name from chain key: flowEvm → FLOW_EVM, base → BASE
  function envKey(key) {
    return key.replace(/([a-z])([A-Z])/g, '$1_$2').toUpperCase();
  }

  // Build defineChain blocks — RPC can be overridden via VITE_<CHAIN>_RPC env var
  const chainDefs = enabledChains.map(([key, chain]) => {
    const varName = key + 'Mainnet';
    const envName = `VITE_${envKey(key)}_RPC`;
    return `export const ${varName} = defineChain({
  id: ${chain.chainId},
  name: '${esc(chain.name)}',
  nativeCurrency: { decimals: ${chain.nativeCurrency.decimals}, name: '${esc(chain.nativeCurrency.name)}', symbol: '${esc(chain.nativeCurrency.symbol)}' },
  rpcUrls: { default: { http: [import.meta.env.${envName} || '${esc(chain.rpcUrl)}'] } },
  blockExplorers: { default: { name: '${esc(chain.blockExplorerName)}', url: '${esc(chain.blockExplorer)}' } },
})`;
  }).join('\n\n');

  // Build CHAIN_CONFIGS entries
  const configEntries = enabledChains.map(([key, chain]) => {
    const varName = key + 'Mainnet';
    return `  ${key}: {
    key: '${esc(key)}',
    chain: ${varName},
    name: '${esc(chain.name)}',
    shortName: '${esc(chain.shortName)}',
    usdc: '${chain.usdc}',
    policyManager: DEPLOYMENTS[${chain.chainId}]?.contracts.policyManager as \`0x\${string}\` | undefined,
    deployBlock: DEPLOYMENTS[${chain.chainId}]?.deployBlock,
    explorer: '${esc(chain.blockExplorer)}',
    supportsLifi: ${chain.supportsLifi === true},
    enabled: true,
  }`;
  }).join(',\n');

  const firstKey = enabledChains[0][0];

  const output = `// Auto-generated by contracts/scripts/generate-frontend-chains.js
// Do not edit manually - run 'make sync' in contracts/ to regenerate

import { defineChain, type Chain } from 'viem'
import { DEPLOYMENTS } from './deployments'

${chainDefs}

export interface ChainConfig {
  key: string
  chain: Chain
  name: string
  shortName: string
  usdc: \`0x\${string}\`
  policyManager?: \`0x\${string}\`
  deployBlock?: number
  explorer: string
  supportsLifi: boolean
  enabled: boolean
}

export const CHAIN_CONFIGS = {
${configEntries}
} satisfies Record<string, ChainConfig>

export type ChainKey = keyof typeof CHAIN_CONFIGS
export const ENABLED_CHAINS = Object.values(CHAIN_CONFIGS).filter(c => c.enabled)
export const DEFAULT_CHAIN = (import.meta.env.VITE_DEFAULT_CHAIN || '${esc(firstKey)}') as ChainKey
`;

  fs.mkdirSync(path.dirname(OUTPUT_FILE), { recursive: true });
  fs.writeFileSync(OUTPUT_FILE, output);
  console.log(`  ✓ Generated ${OUTPUT_FILE}`);
}

generate();
