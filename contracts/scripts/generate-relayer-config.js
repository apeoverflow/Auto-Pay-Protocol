#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

const DEPLOYMENTS_DIR = 'deployments';
const REGISTRY_FILE = 'chains.json';
const OUTPUT_FILE = '../relayer/src/contracts.ts';

function loadRegistry() {
  if (!fs.existsSync(REGISTRY_FILE)) {
    console.error('Error: chains.json not found');
    process.exit(1);
  }
  return JSON.parse(fs.readFileSync(REGISTRY_FILE, 'utf8'));
}

function loadDeployments() {
  const deployments = {};

  if (!fs.existsSync(DEPLOYMENTS_DIR)) {
    return deployments;
  }

  const files = fs.readdirSync(DEPLOYMENTS_DIR).filter(f => f.endsWith('.json'));

  for (const file of files) {
    const data = JSON.parse(fs.readFileSync(path.join(DEPLOYMENTS_DIR, file)));
    deployments[data.chainId] = data;
  }

  return deployments;
}

function generate() {
  const registry = loadRegistry();
  const deployments = loadDeployments();

  // Build chain configs with deployment info
  const chainConfigs = {};

  for (const [key, chain] of Object.entries(registry)) {
    const deployment = deployments[chain.chainId];

    chainConfigs[key] = {
      chainId: chain.chainId,
      name: chain.name,
      rpcUrl: chain.rpcUrl,
      pollIntervalMs: chain.pollIntervalMs,
      batchSize: chain.batchSize,
      confirmations: chain.confirmations,
      policyManagerAddress: deployment?.contracts?.policyManager || null,
      startBlock: deployment?.deployBlock || 0,
      enabled: chain.enabled && !!deployment?.contracts?.policyManager,
      gasEstimationDivisor: chain.gasEstimationDivisor || undefined,
    };
  }

  const output = `// Auto-generated by contracts/scripts/generate-relayer-config.js
// Do not edit manually - run 'make sync' in contracts/ to regenerate

import type { ChainConfig } from './types.js'

// Chain configurations with deployment addresses
export const CHAIN_CONFIGS: Record<string, ChainConfig> = {
${Object.entries(chainConfigs).map(([key, config]) => `  ${key}: {
    chainId: ${config.chainId},
    name: '${config.name}',
    rpcUrl: process.env.${key.replace(/([a-z])([A-Z])/g, '$1_$2').toUpperCase()}_RPC || '${config.rpcUrl}',
    policyManagerAddress: '${config.policyManagerAddress || '0x0000000000000000000000000000000000000000'}' as \`0x\${string}\`,
    startBlock: ${config.startBlock},
    pollIntervalMs: ${config.pollIntervalMs},
    batchSize: ${config.batchSize},
    confirmations: ${config.confirmations},
    enabled: ${config.enabled},${config.gasEstimationDivisor ? `
    gasEstimationDivisor: ${config.gasEstimationDivisor},` : ''}
  }`).join(',\n')}
} as const

// Deployment metadata
export const DEPLOYMENTS = ${JSON.stringify(deployments, null, 2)} as const

// Get enabled chains, filtered by ENABLED_CHAINS env var if set.
// Usage: ENABLED_CHAINS=baseSepolia (staging) or ENABLED_CHAINS=flowEvm,base (production)
// If unset, returns all chains with enabled: true in their config.
export function getEnabledChainConfigs(): ChainConfig[] {
  const override = process.env.ENABLED_CHAINS?.split(',').map(s => s.trim()).filter(Boolean)
  if (override?.length) {
    return Object.entries(CHAIN_CONFIGS)
      .filter(([key]) => override.includes(key))
      .map(([, c]) => c)
  }
  return Object.values(CHAIN_CONFIGS).filter(c => c.enabled)
}

// Get chain config by ID
export function getChainConfigById(chainId: number): ChainConfig | undefined {
  return Object.values(CHAIN_CONFIGS).find(c => c.chainId === chainId)
}
`;

  fs.mkdirSync(path.dirname(OUTPUT_FILE), { recursive: true });
  fs.writeFileSync(OUTPUT_FILE, output);
  console.log(`  ✓ Generated ${OUTPUT_FILE}`);
}

generate();
