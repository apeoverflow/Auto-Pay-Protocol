#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

const REGISTRY_FILE = 'chains.json';
const DEPLOYMENTS_DIR = 'deployments';
const OUTPUT_FILE = '../packages/agent-sdk/src/chains.ts';

function loadRegistry() {
  if (!fs.existsSync(REGISTRY_FILE)) {
    console.error('Error: chains.json not found');
    process.exit(1);
  }
  return JSON.parse(fs.readFileSync(REGISTRY_FILE, 'utf8'));
}

function loadDeployments() {
  const deployments = {};
  if (!fs.existsSync(DEPLOYMENTS_DIR)) return deployments;

  const files = fs.readdirSync(DEPLOYMENTS_DIR).filter(f => f.endsWith('.json'));
  for (const file of files) {
    const data = JSON.parse(fs.readFileSync(path.join(DEPLOYMENTS_DIR, file)));
    deployments[data.chainId] = data;
  }
  return deployments;
}

function esc(s) {
  return String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}

function generate() {
  const registry = loadRegistry();
  const deployments = loadDeployments();
  const allChains = Object.entries(registry);

  // Build chain key union type
  const chainKeyUnion = allChains.map(([key]) => `'${key}'`).join(' | ');

  // Build chain entries
  const chainEntries = allChains.map(([key, chain]) => {
    const deployment = deployments[chain.chainId];
    const policyManager = deployment?.contracts?.policyManager || '0x0000000000000000000000000000000000000000';
    const nc = chain.nativeCurrency;

    return `  ${key}: {
    name: '${esc(chain.name)}',
    chainId: ${chain.chainId},
    rpcUrl: '${esc(chain.rpcUrl)}',
    policyManager: '${policyManager}',
    usdc: '${chain.usdc}',
    explorer: '${esc(chain.blockExplorer)}',
    nativeCurrency: { name: '${esc(nc.name)}', symbol: '${esc(nc.symbol)}', decimals: ${nc.decimals} },
  }`;
  }).join(',\n');

  const output = `// Auto-generated by contracts/scripts/generate-agent-sdk-chains.js
// Do not edit manually - run 'make sync' in contracts/ to regenerate

export type ChainKey = ${chainKeyUnion}

export interface NativeCurrency {
  name: string
  symbol: string
  decimals: number
}

export interface AgentChainConfig {
  name: string
  chainId: number
  rpcUrl: string
  policyManager: \`0x\${string}\`
  usdc: \`0x\${string}\`
  explorer: string
  nativeCurrency: NativeCurrency
}

export const chains: Record<ChainKey, AgentChainConfig> = {
${chainEntries}
}
`;

  fs.mkdirSync(path.dirname(OUTPUT_FILE), { recursive: true });
  fs.writeFileSync(OUTPUT_FILE, output);
  console.log(`  ✓ Generated ${OUTPUT_FILE}`);
}

generate();
